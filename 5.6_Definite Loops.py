# We call the WHILE statement an indefinite loop because it simply loops until some condition becomes False,
# whereas the FOR loop is looping through a known set of items so it runs through as
# many iterations as there are items in the set.

# n particular, friend is the iteration variable for the for loop.

friends = ['Joseph', 'Glen', 'Sally', 'Carson']
for x in friends:
    print('Happy New Year:', x)
print('Done!')